#!/bin/bash

#
# Script para mostrar la información de Bash.
#

clear
echo "Nombre de Bash: $BASH"; # Mostrará por pantalla el nombre de Bash.
echo "Versión de Bash: $BASH_VERSION"; # Mostrará por pantalla la versión de Bash.
echo "Número de columnas mostradas en pantalla: $COLUMNS"; # Mostrará por pantalla el número de columnas mostradas en pantalla.
echo "Número de columnas mostradas en pantalla: $LINES"; # Mostrará por pantalla el número de columnas mostradas en pantalla.
exit 0
